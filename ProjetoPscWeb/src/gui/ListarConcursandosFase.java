    public ListarConcursandosFase(Fase fase) {
        initComponents();
        try {
            fase = fachada.consultarFasePorId(fase.getId());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
        this.fase = fase;
        for (DiaFase diaFase : fase.getDiasFase()) {
            for (Prova prova : diaFase.getProvas()) {
                provas.add(prova);
            }
        }
        this.carregarListaConcursandos();
        this.carregarListaProvasFase();
    }

