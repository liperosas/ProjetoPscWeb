    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        try {
            // TODO add your handling code here:
            concurso.getEmpresa().setId(codEmpresa[jComboBox1.getSelectedIndex()]);
            concurso.setNomeConcurso(jTextField1.getText());
            concurso.setAreasConcurso(areaConcursos);
            SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
            concurso.getDatainscricao().setTime(df.parse(TextDataInicio.getText()));
            concurso.getDatafinal().setTime(df.parse(TextDataFinal.getText()));
            for (AreaConcurso ac : concurso.getAreasConcurso()) {
                ac.setConcurso(concurso);
            }
            fachada.inserirConcurso(concurso);
            JOptionPane.showMessageDialog(rootPane, "Concurso inserido com sucesso");
            lC.carregarTabelaConcurso();
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jButton8ActionPerformed

