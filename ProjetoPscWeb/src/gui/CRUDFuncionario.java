    private void buttonSalvarFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSalvarFuncionarioActionPerformed
        // TODO add your handling code here:
                   
        try {
            Funcionario funcionario = new Funcionario();
            
            funcionario.setNome(textNomeFuncionario.getText());
            funcionario.setCpf(textCPFFuncionario.getText());
            funcionario.setRg(textRGFuncionario.getText());
            funcionario.setTelefone(textTelefoneFuncionario.getText());
            funcionario.setCelular(textCelularFuncionario.getText());
            funcionario.setSalario(Double.parseDouble(textSalarioFuncionario.getText()));
            funcionario.setCtps(textCTPSFuncionario.getText());
            Endereco endereco = new Endereco();
            endereco.setCep(textCEPFuncionario.getText());
            endereco.setBairro(textBairroFuncionario.getText());
            endereco.setCidade(textCidadeFuncionario.getText());
            endereco.setComplemento(textComplementoFuncionario.getText());
            endereco.setNumero(textNumeroFuncionario.getText());
            endereco.setUf(comboUFFuncionario.getSelectedItem().toString());
            endereco.setLogradouro(textLogradouroFuncionario.getText());
            funcionario.setEndereco(endereco);
            Calendar c = Calendar.getInstance();
            DateFormat f = DateFormat.getDateInstance();
            Date d = f.parse(textDataNascFuncionario.getText());
            c.setTime(d);
            funcionario.setData_nasc(c);
            fachada.inserirFuncionario(funcionario);
            JOptionPane.showMessageDialog(rootPane, "Funcionário cadastrado com sucesso");
            lf.carregarTabelaFucinonarios();
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_buttonSalvarFuncionarioActionPerformed

