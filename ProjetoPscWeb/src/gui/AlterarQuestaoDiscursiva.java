    private void ButtonAlterarQuestaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonAlterarQuestaoActionPerformed
        try {
            // TODO add your handling code here:
            questaoDiscursiva.setId(codigo);
            questaoDiscursiva.setLinhas(Integer.parseInt(textNumLinhas.getText()));
            questaoDiscursiva.setTexto(textAreaTextoQuestao.getText());
            questaoDiscursiva.setResposta(textAreaRespostaQuestao.getText());
            if (JOptionPane.showConfirmDialog(rootPane, "VocÃª realmente quer Alterar esta questÃ£o?", "Alterar QuestÃ£o", JOptionPane.YES_NO_OPTION) == 0) {
                fachada.atualizarQuestaoDiscursiva(questaoDiscursiva);
                JOptionPane.showMessageDialog(rootPane, "QuestÃ£o Alterar com sucesso");
                lquestao.carregarListaQuestoesDiscursiva();
                this.dispose();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_ButtonAlterarQuestaoActionPerformed

