    public CRUDQuestao(ListarQuestao lquestao) {
        try {
            initComponents();
            //List<Genero> generos = new ArrayList<>();
            //generos = fachada.consultarTodosGenero();
            this.lquestao = lquestao;
            generos = (ArrayList<Genero>) fachada.consultarTodosGenero();
            for (Genero genero : generos) {
                ComboGeneroQuestao.addItem(genero.getGenero());
            }
            List<Elaborador> elaboradores = new ArrayList<Elaborador>();
            elaboradores = fachada.consultarTodosElaborador();
            codElaboradores = new long[elaboradores.size()];
            int i = 0;
            for (Elaborador elaborador : elaboradores) {
                comboElaboradorQuestao.addItem(elaborador.getNome());
                codElaboradores[i] = elaborador.getId();
                i++;
            }
            radioDiscursiva.setSelected(true);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }

    }

