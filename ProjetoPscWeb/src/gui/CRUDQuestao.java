    private void ButtonSalvarQuestaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSalvarQuestaoActionPerformed
        // TODO add your handling code here:
        try {
            Elaborador elaborador = new Elaborador();
            elaborador.setId(codElaboradores[comboElaboradorQuestao.getSelectedIndex()]);
            Genero genero = new Genero();
            genero = generos.get(ComboGeneroQuestao.getSelectedIndex());
            if (radioMultiplaEscolha.isSelected()) {
                questaoMultipla.setElaborador(elaborador);
                //questaoMultipla.setGenero(null);
                questaoMultipla.setReferencia(textReferenciaQuestao.getText());
                questaoMultipla.setTexto(textAreaTextoQuestao.getText());
                questaoMultipla.setGenero(genero);
                for (Alternativa alternativa : questaoMultipla.getAlternativas()) {
                    alternativa.setQuestao(questaoMultipla);
                }
                fachada.inserirQuestaoMultiplaEscolha(questaoMultipla);
                lquestao.carregarListaQuestoesMultiplaEscolha();
            } else if (radioDiscursiva.isSelected()) {
                questaoDiscursiva.setElaborador(elaborador);
                questaoDiscursiva.setResposta(textResposta.getText());
                questaoDiscursiva.setLinhas(Integer.parseInt(textNumLinhas.getText()));
                questaoDiscursiva.setTexto(textAreaTextoQuestao.getText());
                questaoDiscursiva.setGenero(genero);
                fachada.inserirQuestaoDiscursiva(questaoDiscursiva);
                lquestao.carregarListaQuestoesDiscursiva();
            }
            JOptionPane.showMessageDialog(rootPane, "Questão Cadastrada com Sucesso");
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_ButtonSalvarQuestaoActionPerformed

