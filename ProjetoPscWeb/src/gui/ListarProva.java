    public void carregarTabelaProva() {
        try {
            modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new Object[]{"Id", "Genero", "AreaConcurso", "Concurso", "Fase", "DiaFase"});
            provas = new ArrayList<Prova>();
            provas = (ArrayList<Prova>) fachada.consultarTodosProva();
            String fase;
            String dataFase;
            for (Prova prova : provas) {
                modelo.addRow(new String[]{Long.toString(prova.getId()), prova.getGenero().getGenero(), prova.getDiaFase().getFase().getAreaconcurso().getNome(), prova.getDiaFase().getFase().getAreaconcurso().getConcurso().getNomeConcurso(), fase = Long.toString(prova.getDiaFase().getFase().getId()), (prova.getDiaFase().getDataDia().get(5) + "/" + prova.getDiaFase().getDataDia().get(2) + "/" + prova.getDiaFase().getDataDia().get(1))});
            }
            TableListarProva.setModel(modelo);
            // TODO add your handling code here:
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }

