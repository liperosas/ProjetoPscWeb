    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            if (TableListarProva.getSelectedRow() != -1) {
                List<Gabarito> gabaritos = fachada.consultarGabaritoProva(provas.get(TableListarProva.getSelectedRow()).getId());
                if (gabaritos.size() > 0) {
                    if (JOptionPane.showConfirmDialog(rootPane, "Esta prova j√° possui um gabarito. \n Gostaria de altera-lo? ", "Alterar Gabarito", JOptionPane.YES_NO_OPTION) == 0) {
                        AlterarGabaritoProva agp = new AlterarGabaritoProva(provas.get(TableListarProva.getSelectedRow()), gabaritos.get(0));
                        agp.setVisible(true);
                    }
                } else {
                    CRUDGabaritoProva cgp = new CRUDGabaritoProva(provas.get(TableListarProva.getSelectedRow()));
                    cgp.setVisible(true);
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

